"""Quick (ad-hoc) one-shot prompt fan-out.

This is the lightest possible BYOT entry point: no YAML required up
front. The user types a prompt, picks models, gets a side-by-side
report, and can optionally persist the prompt as a reusable custom
suite so it shows up in ``spark-bench shell`` → ``Custom`` next time.

Internally it just builds a one-task ``CustomSuiteDefinition`` in
memory and feeds it to ``run_custom_suite_quick``. There is exactly
one runner, one summary format, one results layout — ``quick`` is
just a nicer front door for the common "what do all my models say
to *this* prompt?" workflow.

Surfaces:

- ``spark-bench quick "your prompt here" --experiment ... --platform spark``
  — non-interactive CLI command.
- ``spark-bench shell`` → ``Quick`` menu entry — interactive TUI flow.

Saved prompts default to ``examples/custom-tests/quick-saved/<slug>/suite.yaml``
under the repo. The directory is git-ignored so saved prompts do not
pollute ``git status``; the existing ``shell.discover_custom_suites``
helper still picks them up because they live under
``examples/custom-tests/``.
"""

from __future__ import annotations

import re
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from spark_benchmark.custom_suites import (
    CustomSuiteDefinition,
    CustomSuiteTask,
    slugify_suite_name,
)
from spark_benchmark.models import SamplingConfig

QUICK_TASK_ID = "ad-hoc"

# Saved-suite default location, expressed as a tuple so callers can
# join it onto any repo root without OS-specific separators leaking
# into module-level state.
DEFAULT_SAVE_SUBDIR: tuple[str, ...] = ("examples", "custom-tests", "quick-saved")


def _slugify_prompt_head(prompt: str, max_chars: int = 40) -> str:
    """Filesystem-safe slug from the first ``max_chars`` of ``prompt``.

    Falls back to ``"ad-hoc"`` if the prompt is empty or contains no
    alphanumerics in its first line.
    """
    stripped = prompt.strip()
    if not stripped:
        return "ad-hoc"
    head = stripped.splitlines()[0][:max_chars]
    slug = re.sub(r"[^A-Za-z0-9]+", "-", head).strip("-").lower()
    return slug or "ad-hoc"


def default_quick_suite_name(prompt: str) -> str:
    """Human-readable suite name derived from a prompt's first words.

    Example: ``"What is the capital of Czechia?"`` →
    ``"quick-what-is-the-capital-of-czechia"``.
    """
    return f"quick-{_slugify_prompt_head(prompt)}"


def build_quick_suite(
    prompt: str,
    *,
    name: str | None = None,
    sampling: SamplingConfig | None = None,
    description: str | None = None,
) -> CustomSuiteDefinition:
    """Synthesize an in-memory one-task custom suite from a single prompt.

    The returned object is identical in shape to anything
    ``load_custom_suite`` would have produced from a YAML on disk —
    the rest of the pipeline cannot tell the difference.
    """
    if not prompt.strip():
        raise ValueError("quick prompt must not be empty")
    suite_name = (name or default_quick_suite_name(prompt)).strip()
    if not suite_name:
        raise ValueError("quick suite name must not be empty after stripping")
    return CustomSuiteDefinition(
        name=suite_name,
        version="1.0",
        description=description
        or "Ad-hoc one-shot prompt fan-out (spark-bench quick).",
        mode="quick",
        sampling=sampling or SamplingConfig(),
        tasks=[
            CustomSuiteTask(
                task_id=QUICK_TASK_ID,
                prompt=prompt,
            )
        ],
        notes=["auto-generated by spark-bench quick"],
    )


def default_save_dir(repo_root: Path) -> Path:
    """The repo-local save directory for persisted quick prompts.

    Lives at ``examples/custom-tests/quick-saved/`` so the existing
    discovery helper (``shell.discover_custom_suites``) picks it up
    automatically. The path is git-ignored.
    """
    return repo_root.joinpath(*DEFAULT_SAVE_SUBDIR)


def save_quick_suite_as_yaml(
    suite: CustomSuiteDefinition,
    *,
    save_root: Path,
    name: str | None = None,
    overwrite: bool = False,
) -> Path:
    """Emit ``save_root/<slug>/suite.yaml`` and return its path.

    ``name`` overrides the on-disk directory slug (defaults to the
    suite's own name slugified via ``slugify_suite_name``).

    ``overwrite=False`` (the default) refuses to clobber an existing
    file with ``FileExistsError`` so the caller can prompt the user.
    """
    try:
        import yaml  # type: ignore
    except ImportError as exc:  # pragma: no cover - PyYAML is in pyproject
        raise RuntimeError(
            "PyYAML is required to write quick-suite YAML files. "
            "Install spark-benchmark with its full dependency set."
        ) from exc

    slug = slugify_suite_name(name or suite.name)
    target_dir = save_root / slug
    target_path = target_dir / "suite.yaml"
    if target_path.exists() and not overwrite:
        raise FileExistsError(target_path)
    target_dir.mkdir(parents=True, exist_ok=True)

    payload: dict[str, Any] = suite.model_dump(mode="json")
    # Tidy-up: drop empty / null fields so the emitted YAML is short
    # and easy to edit. The schema defaults handle re-loading.
    for task in payload.get("tasks", []):
        for key in ("sampling", "timeout_s"):
            if task.get(key) is None:
                task.pop(key, None)
        if not task.get("tags"):
            task.pop("tags", None)
        if not task.get("notes"):
            task.pop("notes", None)
    if not payload.get("models"):
        payload.pop("models", None)
    if not payload.get("notes"):
        payload.pop("notes", None)
    if not payload.get("description"):
        payload.pop("description", None)

    header = (
        f"# Auto-generated by spark-bench quick at "
        f"{datetime.now(timezone.utc).strftime('%Y-%m-%dT%H:%M:%SZ')}.\n"
        f"# Edit freely; this is just a regular custom suite YAML.\n"
        f"# See docs/custom-tests-spec.md for the full schema.\n\n"
    )
    body = yaml.safe_dump(payload, sort_keys=False, allow_unicode=True)
    target_path.write_text(header + body, encoding="utf-8")
    return target_path
