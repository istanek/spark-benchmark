def test_default_save_dir_is_under_examples_custom_tests() -> None:
    repo = Path("/tmp/some-repo")
    assert default_save_dir(repo) == repo.joinpath(*DEFAULT_SAVE_SUBDIR)
    # The constant itself stays anchored at examples/custom-tests/quick-saved
    # so discover_custom_suites picks the saved YAMLs up out of the box.
    assert DEFAULT_SAVE_SUBDIR == ("examples", "custom-tests", "quick-saved")


def test_save_quick_suite_yaml_round_trips_through_loader() -> None:
    suite = build_quick_suite("Co je hlavní město Česka?", name="capital-cz")
    with TemporaryDirectory() as tmp:
        save_root = Path(tmp)
        path = save_quick_suite_as_yaml(suite, save_root=save_root)
        assert path == save_root / "capital-cz" / "suite.yaml"
        assert path.exists()
        # The YAML must be loadable by the canonical loader and produce
        # an equivalent suite (same name, same single task, same prompt).
        reloaded = load_custom_suite(path)
        assert reloaded.name == suite.name
        assert reloaded.mode == suite.mode
        assert len(reloaded.tasks) == 1
        assert reloaded.tasks[0].task_id == QUICK_TASK_ID
        assert reloaded.tasks[0].prompt == suite.tasks[0].prompt
        # Auto-generated header is on disk so the user knows where it came from.
        text = path.read_text(encoding="utf-8")
        assert text.startswith("# Auto-generated by spark-bench quick")


def test_save_quick_suite_refuses_to_overwrite_by_default() -> None:
    suite = build_quick_suite("hi", name="dup")
    with TemporaryDirectory() as tmp:
        save_root = Path(tmp)
        save_quick_suite_as_yaml(suite, save_root=save_root)
        try:
            save_quick_suite_as_yaml(suite, save_root=save_root)
        except FileExistsError as exc:
            assert "suite.yaml" in str(exc)
        else:
            raise AssertionError("expected FileExistsError on second save")


def test_save_quick_suite_overwrite_true_replaces_target() -> None:
    suite_a = build_quick_suite("first prompt", name="reuse")
    suite_b = build_quick_suite("second prompt — much different", name="reuse")
    with TemporaryDirectory() as tmp:
        save_root = Path(tmp)
        path = save_quick_suite_as_yaml(suite_a, save_root=save_root)
        save_quick_suite_as_yaml(suite_b, save_root=save_root, overwrite=True)
        reloaded = load_custom_suite(path)
        assert reloaded.tasks[0].prompt == "second prompt — much different"


def test_save_quick_suite_yaml_strips_empty_optional_fields() -> None:
    """Empty `models`, `notes`, etc. are dropped from the emitted YAML."""
    suite = build_quick_suite("a tidy prompt", name="tidy")
    with TemporaryDirectory() as tmp:
        path = save_quick_suite_as_yaml(suite, save_root=Path(tmp))
        text = path.read_text(encoding="utf-8")
        # `models: []` and `notes: []` are noise for a one-prompt YAML; they
        # should not appear when empty. (`notes` at top-level *is* set by
        # build_quick_suite, so it's allowed to appear.)
        assert "models:" not in text  # empty list pruned
        # Per-task: tags is empty → pruned.
        assert "tags:" not in text


