def test_build_quick_suite_yields_exactly_one_task() -> None:
    suite = build_quick_suite("Translate this idiom into English: 'Háže hrách na zeď'.")
    assert isinstance(suite, CustomSuiteDefinition)
    assert suite.mode == "quick"
    assert len(suite.tasks) == 1
    assert suite.tasks[0].task_id == QUICK_TASK_ID
    assert suite.tasks[0].prompt.startswith("Translate this idiom")
    assert "auto-generated by spark-bench quick" in (suite.notes or [""])[0]


def test_build_quick_suite_default_name_is_quick_prefixed_slug() -> None:
    suite = build_quick_suite("What is the capital of Czechia?")
    assert suite.name.startswith("quick-")
    # Slug must be filesystem-safe (no spaces, no punctuation).
    assert all(c.isalnum() or c == "-" for c in suite.name)
    assert default_quick_suite_name("What is the capital of Czechia?") == suite.name


def test_build_quick_suite_explicit_name_overrides_default() -> None:
    suite = build_quick_suite("anything", name="my-test")
    assert suite.name == "my-test"


def test_build_quick_suite_rejects_empty_prompt() -> None:
    for empty in ("", "   ", "\n\n"):
        try:
            build_quick_suite(empty)
        except ValueError as exc:
            assert "must not be empty" in str(exc)
        else:
            raise AssertionError(f"expected ValueError for prompt {empty!r}")


def test_build_quick_suite_passes_through_sampling() -> None:
    sampling = SamplingConfig(temperature=0.7, max_tokens=128, seed=99)
    suite = build_quick_suite("hi", sampling=sampling)
    # Suite default carries the user-provided sampling so per-task fall-through works.
    assert suite.sampling.temperature == 0.7
    assert suite.sampling.max_tokens == 128
    assert suite.sampling.seed == 99


def test_default_quick_suite_name_falls_back_when_prompt_is_punctuation_only() -> None:
    # All non-alphanumerics → slug collapses to empty; helper falls back to "ad-hoc".
    assert default_quick_suite_name("!!! ??? ...") == "quick-ad-hoc"


