def _make_model(name: str, tag: str) -> ModelConfig:
    return ModelConfig(
        name=name,
        family=name.split("-")[0],
        revision=tag,
        quantization="ollama-default",
        source="ollama-local",
        context_length=4096,
        artifact_path=tag,
    )


def _make_backend() -> BackendConfig:
    return BackendConfig(
        name=BackendKind.OLLAMA,
        entrypoint="ollama",
        version="local",
        transport="http",
        options={"endpoint": "http://localhost:11434/api/generate"},
    )


class _FakeBackend:
    """Backend stub that returns a deterministic GenerationResult per call.

    Records load_model / generate / unload events so we can assert the
    runner contract without smuggling task IDs.
    """

    def __init__(self) -> None:
        self._loaded: ModelConfig | None = None
        self.calls: list[tuple[str, str]] = []

    def load_model(self, cfg: ModelConfig) -> None:
        self._loaded = cfg
        self.calls.append(("load", cfg.name))

    def unload(self) -> None:
        if self._loaded is not None:
            self.calls.append(("unload", self._loaded.name))
        self._loaded = None

    def generate(self, prompt: str, sampling: SamplingConfig) -> GenerationResult:
        assert self._loaded is not None
        self.calls.append(("generate", self._loaded.name))
        return GenerationResult(
            prompt=prompt,
            output=f"reply from {self._loaded.name} to {prompt[:40]!r}",
            finish_reason="stop",
            metrics=InferenceMetrics(
                prefill_tokens=10,
                decode_tokens=20,
                prefill_time_s=0.1,
                decode_time_s=0.5,
                ttft_ms=120.0,
                peak_memory_mb=4096.0,
                backend_version="local",
                quantization="ollama-default",
            ),
        )


