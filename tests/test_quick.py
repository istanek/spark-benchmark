def test_quick_suite_end_to_end_runs_one_prompt_against_each_model() -> None:
    suite = build_quick_suite("Hello world?")
    backend = _FakeBackend()
    models = [
        _make_model("qwen-3.6", "qwen3.6:35b"),
        _make_model("gemma-4", "gemma4:31b"),
    ]
    with TemporaryDirectory() as tmp:
        run_dir = Path(tmp)
        summary = run_custom_suite_quick(
            suite=suite,
            backend=backend,
            backend_config=_make_backend(),
            model_configs=models,
            run_dir=run_dir,
            default_sampling=suite.sampling,
        )
        # One task × two models = two rows, each tagged with the ad-hoc id.
        rows = [
            json.loads(line)
            for line in (run_dir / "results.jsonl").read_text(encoding="utf-8").splitlines()
            if line.strip()
        ]
        assert len(rows) == 2
        assert {row["task_id"] for row in rows} == {QUICK_TASK_ID}
        assert {row["model"] for row in rows} == {"qwen-3.6", "gemma-4"}
        # Per-model bucket reflects the single task each.
        per_model = {bucket["model"]: bucket for bucket in summary["per_model"]}
        assert per_model["qwen-3.6"]["tasks_completed"] == 1
        assert per_model["gemma-4"]["tasks_completed"] == 1
        # Side-by-side markdown summary is rendered.
        md = (run_dir / "summary.md").read_text(encoding="utf-8")
        assert f"### Task `{QUICK_TASK_ID}`" in md


